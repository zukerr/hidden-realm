using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SerialPosition
{
    public float posX;
    public float posY;
    public float rotZ;

    public SerialPosition(float posX, float posY, float rotZ)
    {
        this.posX = posX;
        this.posY = posY;
        this.rotZ = rotZ;
    }
}
